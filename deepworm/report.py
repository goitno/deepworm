"""Report formatting and output utilities."""

from __future__ import annotations

import json
import re
from pathlib import Path
from typing import Optional

from rich.console import Console
from rich.markdown import Markdown


console = Console()


def print_report(report: str) -> None:
    """Print a markdown report to the terminal with rich formatting."""
    md = Markdown(report)
    console.print()
    console.print(md)
    console.print()


def save_report(
    report: str,
    path: str | None = None,
    topic: str = "",
    fmt: str = "markdown",
) -> str:
    """Save a report to a file. Returns the path used.

    Formats: markdown (default), text, json, html
    """
    if path is None:
        slug = _slugify(topic) if topic else "research"
        ext = {"markdown": ".md", "text": ".txt", "json": ".json", "html": ".html"}.get(fmt, ".md")
        path = f"{slug}{ext}"

    filepath = Path(path)
    filepath.parent.mkdir(parents=True, exist_ok=True)

    if fmt == "json":
        data = {"topic": topic, "report": report}
        filepath.write_text(json.dumps(data, indent=2, ensure_ascii=False), encoding="utf-8")
    elif fmt == "text":
        plain = _markdown_to_text(report)
        filepath.write_text(plain, encoding="utf-8")
    elif fmt == "html":
        html = markdown_to_html(report, topic=topic)
        filepath.write_text(html, encoding="utf-8")
    else:
        filepath.write_text(report, encoding="utf-8")

    return str(filepath)


