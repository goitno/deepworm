def _md_to_html_body(md: str) -> str:
    """Convert markdown text to HTML body (minimal parser, no deps)."""
    lines = md.split("\n")
    html_lines = []
    in_code_block = False
    in_list = False
    list_type = "ul"

    for line in lines:
        # Code blocks
        if line.strip().startswith("```"):
            if in_code_block:
                html_lines.append("</code></pre>")
                in_code_block = False
            else:
                lang = line.strip()[3:].strip()
                html_lines.append(f'<pre><code class="language-{lang}">' if lang else "<pre><code>")
                in_code_block = True
            continue

        if in_code_block:
            html_lines.append(_escape_html(line))
            continue

        # Close list if needed
        if in_list and not re.match(r'^(\s*[-*+]|\s*\d+\.)\s', line) and line.strip():
            html_lines.append(f"</{list_type}>")
            in_list = False

        # Headers
        header_match = re.match(r'^(#{1,6})\s+(.+)', line)
        if header_match:
            level = len(header_match.group(1))
            text = _inline_md(header_match.group(2))
            html_lines.append(f"<h{level}>{text}</h{level}>")
            continue

        # Horizontal rules
        if re.match(r'^(-{3,}|\*{3,}|_{3,})\s*$', line):
            html_lines.append("<hr>")
            continue

        # Unordered lists
        ul_match = re.match(r'^(\s*)[-*+]\s+(.+)', line)
        if ul_match:
            if not in_list:
                html_lines.append("<ul>")
                in_list = True
                list_type = "ul"
            html_lines.append(f"<li>{_inline_md(ul_match.group(2))}</li>")
            continue

        # Ordered lists
        ol_match = re.match(r'^(\s*)\d+\.\s+(.+)', line)
        if ol_match:
            if not in_list:
                html_lines.append("<ol>")
                in_list = True
                list_type = "ol"
            html_lines.append(f"<li>{_inline_md(ol_match.group(2))}</li>")
            continue

        # Blockquotes
        if line.startswith(">"):
            text = _inline_md(line.lstrip("> "))
            html_lines.append(f"<blockquote><p>{text}</p></blockquote>")
            continue

        # Table rows
        if "|" in line:
            cells = [c.strip() for c in line.split("|")[1:-1]]
            if cells and all(re.match(r'^[-:]+$', c) for c in cells):
                continue  # Skip separator row
            if cells:
                is_first_row = not any("<tr>" in l for l in html_lines[-10:])
                tag = "th" if is_first_row else "td"
                if is_first_row:
                    html_lines.append("<table>")
                row = "".join(f"<{tag}>{_inline_md(c)}</{tag}>" for c in cells)
                html_lines.append(f"<tr>{row}</tr>")
                continue

        # Empty lines
        if not line.strip():
            html_lines.append("")
            continue

        # Regular paragraph
        html_lines.append(f"<p>{_inline_md(line)}</p>")

    # Close any open elements
    if in_list:
        html_lines.append(f"</{list_type}>")
    if in_code_block:
        html_lines.append("</code></pre>")

    return "\n".join(html_lines)


def _inline_md(text: str) -> str:
    """Convert inline markdown (bold, italic, code, links) to HTML."""
    # Code (must be first to avoid processing inside code)
    text = re.sub(r'`(.+?)`', r'<code>\1</code>', text)
    # Bold
    text = re.sub(r'\*\*(.+?)\*\*', r'<strong>\1</strong>', text)
    text = re.sub(r'__(.+?)__', r'<strong>\1</strong>', text)
    # Italic
    text = re.sub(r'\*(.+?)\*', r'<em>\1</em>', text)
    text = re.sub(r'_(.+?)_', r'<em>\1</em>', text)
    # Links
    text = re.sub(r'\[(.+?)\]\((.+?)\)', r'<a href="\2">\1</a>', text)
    # Images
    text = re.sub(r'!\[(.+?)\]\((.+?)\)', r'<img src="\2" alt="\1">', text)
    return text


def _escape_html(text: str) -> str:
    """Escape HTML special characters."""
    return (
        text.replace("&", "&amp;")
        .replace("<", "&lt;")
        .replace(">", "&gt;")
        .replace('"', "&quot;")
    )


def _markdown_to_text(md: str) -> str:
    """Basic markdown to plain text conversion."""
    text = md
    text = re.sub(r'^#{1,6}\s+', '', text, flags=re.MULTILINE)
    text = re.sub(r'\*\*(.+?)\*\*', r'\1', text)
    text = re.sub(r'\*(.+?)\*', r'\1', text)
    text = re.sub(r'\[(.+?)\]\(.+?\)', r'\1', text)
    text = re.sub(r'```.*?```', '', text, flags=re.DOTALL)
    text = re.sub(r'`(.+?)`', r'\1', text)
    return text


def _slugify(text: str) -> str:
    """Convert text to a filesystem-safe slug."""
    text = text.lower().strip()
    text = re.sub(r'[^\w\s-]', '', text)
    text = re.sub(r'[\s_]+', '-', text)
    text = re.sub(r'-+', '-', text)
    return text[:60].strip('-')
